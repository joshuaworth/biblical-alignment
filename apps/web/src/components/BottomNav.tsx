'use client'

import Link from 'next/link'
import { usePathname } from 'next/navigation'
import { useSettings } from '@/app/providers'
import { useHaptics } from '@/hooks/useHaptics'

interface NavItem {
  href: string
  label: string
  icon: React.ReactNode
  activeIcon: React.ReactNode
}

const navItems: NavItem[] = [
  {
    href: '/read',
    label: 'Read',
    icon: <ReadIcon />,
    activeIcon: <ReadIconFilled />,
  },
  {
    href: '/search',
    label: 'Search',
    icon: <SearchIcon />,
    activeIcon: <SearchIconFilled />,
  },
  {
    href: '/bookmarks',
    label: 'Bookmarks',
    icon: <BookmarkIcon />,
    activeIcon: <BookmarkIconFilled />,
  },
  {
    href: '/settings',
    label: 'Settings',
    icon: <SettingsIcon />,
    activeIcon: <SettingsIconFilled />,
  },
]

export function BottomNav() {
  const pathname = usePathname()
  const { openSettings } = useSettings()
  const haptics = useHaptics()

  const isActive = (path: string) => {
    if (path === '/read') {
      return pathname === '/' || pathname.startsWith('/read')
    }
    return pathname.startsWith(path)
  }

  const handleNavClick = (e: React.MouseEvent, item: NavItem) => {
    haptics.light()
    // Settings opens a panel instead of navigating
    if (item.href === '/settings') {
      e.preventDefault()
      openSettings()
    }
  }

  return (
    <nav
      className="bottom-nav"
      role="navigation"
      aria-label="Main navigation"
    >
      <div className="bottom-nav-container">
        {navItems.map((item) => {
          const active = isActive(item.href)
          return (
            <Link
              key={item.href}
              href={item.href}
              onClick={(e) => handleNavClick(e, item)}
              className={`bottom-nav-item ${active ? 'active' : ''}`}
              data-haptic="light"
              aria-current={active ? 'page' : undefined}
            >
              <span className="bottom-nav-icon-wrapper">
                {active && <span className="bottom-nav-pill" aria-hidden="true" />}
                <span className="bottom-nav-icon">
                  {active ? item.activeIcon : item.icon}
                </span>
              </span>
              <span className="bottom-nav-label">{item.label}</span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}

